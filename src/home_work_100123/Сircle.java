package home_work_100123;

public class Сircle {
    public static void main(String[] args) {

        double r = 4;                               // радиус
        double с = 2 * Math.PI * r;                 // длина окружности
        double s = Math.PI * Math.pow(r, 2);        // площадь круга

        // выводим
        System.out.println(с);
        System.out.println(s);

    }
}
